import 'package:flutter/material.dart';

class Pokemon {
  final String? nome;
  final String? id;
  final String? tipo;
  final String? hp;
  final String? ataque;
  final String? defesa;
  final String? velocidade;
  final String? total;
  final String? hab1;
  final String? hab2;
  final String? imagem;
  final Color? cor;
  final Color? cor2;

  Pokemon({this.nome, this.id, this.tipo, this.hp, this.ataque, this.defesa, this.velocidade, this.total, this.hab1, this.hab2, this.imagem, this.cor, this.cor2});
}
